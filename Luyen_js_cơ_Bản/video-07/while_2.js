// ví dụ 2:
let y = 9;
while (y < 9) { // chạy từ 0 -10 đến > 10 thì dừng
    console.log(`Count: ${y}`);
    y++;
}
console.log("--------------------------------------------------------------------------------");

// ta thấy được ở ví dụ 2 khi khai báo y = 9 thì nó ko chạy gì cả nó khác với do-while



