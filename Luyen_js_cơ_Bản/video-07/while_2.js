//  trong js , vòng lặp (loop) là một cấu trúc điều kiện để lặp lại một đoạn mã nhiều lần .(ta muốn dùng để lặp lại 1 đoạn code nhiều lần cho đến khi nó không còn đúng nữa)
//  thường được sử dụng dể thực hiện 1 hành động nhiều lần cho đến khi 1 điều kiện nào đó được thỏa mãn .

// while được dùng khi muốn lặp lại 1 đoạn code cho đến khi điều kiện đó không còn đúng nữa
let i = 0;
while (i < 11) { // chạy từ 0 -10 đến > 10 thì dừng
    console.log(`Count: ${i}`);
    i++;
}
console.log("--------------------------------------------------------------------------------");
