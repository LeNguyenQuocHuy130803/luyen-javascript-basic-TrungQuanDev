//do..while : cũng tựa tựa như while. nhưng đoạn mã sẽ được thực hiện ít nhất 1 lần trước khi điều kiện được kiểm tra

let i = 11;
do {
    console.log(`Count: ${i}`);
    i++;
}
while (i < 11) { // 
}
console.log("--------------------------------------------------------------------------------");

// nó thực hiện ít nhất 1 lần trong do , nó sẽ cho ra dc kết quả là Count: 11, nó sẽ thực hiện trong do ( ít nhất là 1 lần ) khác vs while là nó ko chạy luôn khi dkien không đúng

