// logical operation : gọi là toán tử lôgic : AND, OR, NOT

let x = 10;
let y = 5;

console.log(x > 15 && x < 20); // kqua là false vì toán tử và là cả 2 phải cùng đúng
console.log(x > 15 || x < 20); // kqua là true vì toán tử hoặc  là chỉ cần 1 trong 2  đúng
console.log(!( x===y )); // kqua là true vì ! là toán tử ngược lại x bằng y là false mà ngược lại của false là true
console.log(!( x !==y ));// kqua sẽ là false vì ở trong ngoặc x khác y laf đúng và ngược lại của true là false
