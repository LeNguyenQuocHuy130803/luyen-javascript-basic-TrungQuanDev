//  Assignment operators : gọi là toán tử gán
let a = 10; // tức là gán 10 vào cho a
console.log(a);

a += 5; // tức là công 5 vào cho a, bây giờ là a = a+5 mà a ban đầu gán bằng 10 thì 10 +5 =15
console.log(a);

a -= 3; // tức là gán 5 vào cho a, phép toán trên a đã băngf 15 bh trừ cho 3 thì a =12
console.log(a);

a *= 5; // tức là gán 5 vào cho a , là lấy 12*5
console.log(a);

a /= 5; // tức là gán 5 vào cho a, tức là 60/5 
console.log(a);

a %= 5; // chia lấy dư ,tức là gán 5 vào cho a
console.log(a);

a **= 5; // lỹ thừa. tức là gán 5 vào cho a
console.log(a);

